    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (this.isVisible()) {
            String ten = txtTen.getText();
            String regex = "^[a-zA-Z\\sàáạảãâầấậẩẫăằắặẳẵèéẹẻẽêềếệểễìíịỉĩòóọỏõôồốộổỗơờớợởỡùúụủũưừứựửữỳýỵỷỹđÀÁẠẢÃÂẦẤẬẨẪĂẰẮẶẲẴÈÉẸẺẼÊỀẾỆỂỄÌÍỊỈĨÒÓỌỎÕÔỒỐỘỔỖƠỜỚỢỞỠÙÚỤỦŨƯỪỨỰỬỮỲÝỴỶỸĐ]+$";
            // chứa chữ cái và khoảng trắng 
            if (ten.length() > 0) {
                if (!ten.matches(regex)) {// tên sai định dạng
                    JOptionPane.showMessageDialog(this, "Sai định dạng, Nhập lại tên,Vd: Đỗ Thị Thúy Ly");
                    txtTen.requestFocus();
                    return;
                }
            } else {
                JOptionPane.showMessageDialog(this, "Tên không được để trống");
                txtTen.requestFocus();
                return;
            }
        }

        if (this.isVisible()) {
            String sdt = txtSdt.getText();
            String regex = "^(0|\\+84)(3[2-9]|5[689]|7[06-9]|8[1-5]|9[0-9])[0-9]{7}$";//chứa số điện thoại 10 số hay 11 số, đầu 09 or +84
            if (sdt.length() > 0) {
                if (!sdt.matches(regex)) {// địa chỉ sai định dạng
                    JOptionPane.showMessageDialog(this, "Sai định dạng, Nhập lại số điện thoại,VD: 0976766682 hoặc +84961659480");
                    txtSdt.requestFocus();
                    return;
                }
            } else {
                JOptionPane.showMessageDialog(this, "Số điện thoại không được để trống");
                txtSdt.requestFocus();
                return;
            }
        }

        if (this.isVisible()) {
            String email = txtEmail.getText();
            String regex = "^(?=[A-Za-z0-9._%+-]{1,64}@)[A-Za-z0-9._%+-]{1,64}@[A-Za-z0-9.-]{1,255}\\.[A-Za-z]{2,63}$";//chứa chữ cái, số, khoảng trắng, dấu phẩy, dấu chấm, dấu gạch ngang và dấu gạch chéo.
            if (email.length() > 0) {
                if (!email.matches(regex)) {// địa chỉ sai định dạng
                    JOptionPane.showMessageDialog(this, "Sai định dạng, Nhập lại Email,Vd: doly862005@gmail.com");
                    txtEmail.requestFocus();
                    return;
                }
            } else {
                JOptionPane.showMessageDialog(this, "Email không được để trống");
                txtEmail.requestFocus();
                return;
            }
        }

        if (this.isVisible()) {
            String diaChi = txtDiaChi.getText();
            String regex = "^[a-zA-Z0-9\\s,.-/]+$";//chứa chữ cái, số, khoảng trắng, dấu phẩy, dấu chấm, dấu gạch ngang và dấu gạch chéo.
            if (diaChi.length() > 0) {
                if (!diaChi.matches(regex)) {// địa chỉ sai định dạng
                    JOptionPane.showMessageDialog(this, "Sai định dạng, Nhập lại địa chỉ,và k được chứa kí tự đặc biệt: @#$%...");
                    txtDiaChi.requestFocus();
                    return;
                }
            } else {
                JOptionPane.showMessageDialog(this, "Địa chỉ không được để trống");
                txtDiaChi.requestFocus();
                return;
            }
        }

        int chon = JOptionPane.showConfirmDialog(this, "Ban có chắc muốn thêm?");
        if (chon == 0) {
            khachHangRepositories.add(getFormData());
            JOptionPane.showMessageDialog(this, "thêm thành công");
            updateTable(khachHangRepositories.getALLKH());
            txtTen.setText("");
            dateNgaySinh.setDate(null);
            txtSdt.setText("");
            txtEmail.setText("");
            txtDiaChi.setText("");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void tblChonKHMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblChonKHMouseClicked
        // TODO add your handling code here:
        int selectedRow = tblChonKH.getSelectedRow();
        if (selectedRow >= 0) {
            // Giả sử bạn có mô hình bảng với các cột ID khách hàng và ID hóa đơn
            int customerId = (int) tblChonKH.getValueAt(selectedRow, 0); // Cột ID khách hàng
            String invoiceId = bh.getSelectedInvoiceId(); // Lấy ID hóa đơn

            boolean updateSuccessful = banHangSPRepositories.updateKH(customerId, invoiceId);
            if (updateSuccessful) {
                JOptionPane.showMessageDialog(this, "Cập nhật khách hàng thành công!");

                // Giả sử bạn có phương thức để lấy tên khách hàng và số điện thoại
                String customerName = tblChonKH.getValueAt(selectedRow, 2).toString();
                String customerPhone = tblChonKH.getValueAt(selectedRow, 3).toString();

                // Giả sử bạn có các phương thức setText cho các text field trong view Bán hàng
                bh.setCustomerName(customerName);
                bh.setCustomerPhone(customerPhone);
                this.setVisible(false);
            } else {
                JOptionPane.showMessageDialog(this, "Cập nhật khách hàng thất bại!");
            }
        }
    }//GEN-LAST:event_tblChonKHMouseClicked

