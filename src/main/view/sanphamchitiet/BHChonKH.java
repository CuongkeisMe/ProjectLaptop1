    public void updateTable(ArrayList<KhachHang> customers) {
        tableModel.setRowCount(0);

        for (KhachHang kh : customers) {
            tableModel.addRow(new Object[]{kh.getId(), kh.getMa(), kh.getTen(), kh.getSdt()});
        }
    }

